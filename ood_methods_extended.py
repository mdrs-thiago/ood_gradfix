def _to_device(x: torch.Tensor, device: torch.device) -> torch.Tensor:
    return x.to(device, non_blocking=True) if torch.is_tensor(x) else x


def _iter_inputs(loader: Iterable[Batch]):
    """Yield (inputs, labels_or_None) from a dataloader that may yield inputs only."""
    for batch in loader:
        if isinstance(batch, (list, tuple)) and len(batch) == 2:
            yield batch[0], batch[1]
        else:
            yield batch, None


def _find_last_linear(model: torch.nn.Module) -> Tuple[str, torch.nn.Linear]:
    """Find the last torch.nn.Linear module in the model."""
    last_name, last_mod = None, None
    for name, m in model.named_modules():
        if isinstance(m, torch.nn.Linear):
            last_name, last_mod = name, m
    if last_mod is None:
        raise RuntimeError("Could not find a torch.nn.Linear head in the model.")
    return last_name, last_mod


@dataclass
class HeadIO:
    h: torch.Tensor  # penultimate features input to head: (B, d)
    logits: torch.Tensor  # head output logits: (B, K)


class HeadExtractor:
    """Utility to capture penultimate features (input to last Linear) and logits."""

    def __init__(self, model: torch.nn.Module):
        self.model = model
        self.head_name, self.head = _find_last_linear(model)

    def forward(self, x: torch.Tensor) -> HeadIO:
        device = next(self.model.parameters()).device
        x = _to_device(x, device)

        captured: Dict[str, torch.Tensor] = {}

        def pre_hook(_module, inputs):
            # inputs is a tuple; for Linear it's (h,)
            captured["h"] = inputs[0]

        handle = self.head.register_forward_pre_hook(pre_hook)
        try:
            logits = self.model(x)
        finally:
            handle.remove()

        if "h" not in captured:
            raise RuntimeError("Failed to capture penultimate features (head input).")
        h = captured["h"]
        # Ensure 2D (B,d)
        if h.dim() > 2:
            h = h.view(h.size(0), -1)
        return HeadIO(h=h, logits=logits)


def _loss_from_logits(
    logits: torch.Tensor,
    loss_type: str = "uniform_kl",
    temperature: float = 1.0,
) -> torch.Tensor:
    """Return per-sample loss L(x) given logits.

    Supported loss_type:
        - 'uniform_kl': KL(p || U) (proxy via cross-entropy to uniform)
        - 'entropy': negative entropy (encourage high entropy)
        - 'anti_top1': penalize the predicted top-1 probability
        - 'margin': softplus(z2 - z1) using top-1/top-2 logits
        - 'ce_pred': cross-entropy with predicted class (self-target)
    """
    if temperature <= 0:
        raise ValueError("temperature must be > 0")
    z = logits / temperature
    p = torch.softmax(z, dim=1)
    K = p.size(1)

    if loss_type == "uniform_kl":
        # KL(p || U) = sum p log p - sum p log(1/K) = sum p log p + log K
        # up to constant, minimize negative entropy; for gradient signal, use KL explicitly:
        # KL(p||U) = sum p * (log p - log(1/K))
        # We'll compute directly (stable):
        logp = torch.log(p.clamp_min(1e-12))
        loss = (p * (logp + np.log(K))).sum(dim=1)
        return loss

    if loss_type == "entropy":
        # Negative entropy (lower entropy => larger loss)
        logp = torch.log(p.clamp_min(1e-12))
        ent = -(p * logp).sum(dim=1)
        return -ent

    if loss_type == "anti_top1":
        top1 = p.max(dim=1).values
        # -log(1 - top1) (clamped)
        return -torch.log((1.0 - top1).clamp(1e-6, 1.0))

    if loss_type == "margin":
        top2 = torch.topk(z, k=2, dim=1).values
        z1 = top2[:, 0]
        z2 = top2[:, 1]
        return F.softplus(z2 - z1)

    if loss_type == "ce_pred":
        yhat = torch.argmax(z, dim=1)
        return F.cross_entropy(z, yhat, reduction="none")

    raise ValueError(f"Unknown loss_type: {loss_type}")


def _compute_delta(
    logits: torch.Tensor,
    loss_type: str = "uniform_kl",
    temperature: float = 1.0,
) -> torch.Tensor:
    z = logits / temperature
    p = torch.softmax(z, dim=1)
    if loss_type == "uniform_kl":
        logp = torch.log(p.clamp_min(1e-12))
        ent = (p * logp).sum(dim=1, keepdim=True)
        delta = p * (logp - ent) / temperature
    elif loss_type == "entropy":
        logp = torch.log(p.clamp_min(1e-12))
        ent = (p * logp).sum(dim=1, keepdim=True)
        delta = - (p * (logp - ent)) / temperature
    elif loss_type == "ce_pred":
        yhat = torch.argmax(z, dim=1)
        one_hot = torch.zeros_like(p)
        one_hot.scatter_(1, yhat.unsqueeze(1), 1.0)
        delta = (p - one_hot) / temperature
    else:
        logits_req = logits.detach().requires_grad_(True)
        loss = _loss_from_logits(logits_req, loss_type=loss_type, temperature=temperature)
        delta = torch.autograd.grad(loss.sum(), logits_req, retain_graph=False, create_graph=False)[0]
    return delta


