class MSP(OODMethod):
    def compute_ood_scores_features(self, h: torch.Tensor, logits: torch.Tensor) -> torch.Tensor:
        p = torch.softmax(logits, dim=1)
        return 1.0 - p.max(dim=1).values.cpu()


class EnergyBased(OODMethod):
    def __init__(self, model: torch.nn.Module, temperature: float = 1.0):
        super().__init__(model)
        self.temperature = temperature

    def compute_ood_scores_features(self, h: torch.Tensor, logits: torch.Tensor) -> torch.Tensor:
        energy = -torch.logsumexp(logits / self.temperature, dim=1)
        return energy.cpu()


class Odin(OODMethod):
    def __init__(self, model: torch.nn.Module, epsilon: float = 0.001, temperature: float = 1.0):
        super().__init__(model)
        self.epsilon = epsilon
        self.temperature = temperature

    def compute_ood_scores(self, loader: Iterable[Batch]) -> torch.Tensor:
        device = next(self.model.parameters()).device
        scores: List[torch.Tensor] = []
        for x, _ in _iter_inputs(loader):
            x = _to_device(x, device)
            x = x.detach().requires_grad_(True)
            logits = self.model(x) / self.temperature
            # maximize max logit -> minimize negative
            loss = -logits.max(dim=1).values.mean()
            self.model.zero_grad(set_to_none=True)
            if x.grad is not None:
                x.grad.zero_()
            loss.backward()
            x_pert = (x + self.epsilon * x.grad.sign()).detach()
            with torch.no_grad():
                logits_p = self.model(x_pert) / self.temperature
                p = torch.softmax(logits_p, dim=1)
                scores.append((1.0 - p.max(dim=1).values).cpu())
        return torch.cat(scores, dim=0)


