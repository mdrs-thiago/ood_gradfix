def build_method_registry(model: torch.nn.Module) -> Dict[str, OODMethod]:
    """Return a dict of name -> instantiated method with default hyperparameters."""
    return {
        "msp": MSP(model),
        "energy": EnergyBased(model),
        "odin": Odin(model),
        "feat_knn": FeatureKNN(model, k=10),
        "feat_maha": FeatureMahalanobis(model),
        "feat_gmm": FeatureGMM(model, n_components=5),
        "feat_pca": FeaturePCA(model, n_components=0.95),
        "react": ReAct(model, p=90.0),
        "vim": ViM(model),
        "gradnorm": GradNorm(model, loss_type="uniform_kl", p="fro"),
        "gradorth": GradOrth(model, eps_th=0.95, loss_type="uniform_kl"),
        "lowdim_grad_resid": LowDimGradResidual(model, loss_type="uniform_kl", n_components=0.95),
        "gradvec_maha": GradVecMahalanobis(model, loss_type="uniform_kl", n_components=0.95),
        "twosided_resid": TwoSidedHeadGradResidual(model, loss_type="uniform_kl", eps_th_u=0.95, eps_th_v=0.95),
        "twosided_code_maha": TwoSidedHeadGradCodeMahalanobis(model, loss_type="uniform_kl", eps_th_u=0.95, eps_th_v=0.95),
    }


